    "# **Chapter 7: Communication Protocols & Data Formats**\n\n",
    "In a distributed system, every request crosses a network boundary. The choice of *how* services talk to each other — and *what shape* that data takes — affects latency, developer experience, coupling, and scalability. This chapter covers the protocols and serialization formats that underpin modern distributed architectures.\n\n",
    "---\n\n",
    "## **7.1 Synchronous vs. Asynchronous Communication**\n\n",
    "Before choosing a specific protocol, decide whether the caller *waits* for a response or *fires and forgets*.\n\n",
    "| Style | Caller blocks? | Use when |\n",
    "|-------|---------------|----------|\n",
    "| Synchronous (HTTP, gRPC) | Yes | You need an immediate answer (user login, read API) |\n",
    "| Asynchronous (queues, events) | No | Work can happen later (email, analytics, order fulfillment) |\n\n",
    "**Key trade-off**: Synchronous calls are simpler to reason about but create temporal coupling — if the downstream service is slow or down, the caller is blocked or fails.\n\n",
    "---\n\n",
    "## **7.2 REST (Representational State Transfer)**\n\n",
    "**REST** is an architectural style — not a protocol — built on top of HTTP. It is the dominant style for public-facing APIs because HTTP is universally supported and REST is human-readable.\n\n",
    "### **Core Constraints**\n\n",
    "1. **Stateless**: Each request carries all the information the server needs. The server holds no session state between requests.\n",
    "2. **Uniform interface**: Resources are identified by URLs; actions are expressed via HTTP verbs.\n",
    "3. **Client-server separation**: The UI and storage concerns are separated.\n",
    "4. **Cacheable**: Responses explicitly declare whether they can be cached.\n\n",
    "### **HTTP Verbs and their Semantics**\n\n",
    "| Verb | Meaning | Idempotent? | Safe? |\n",
    "|------|---------|-------------|-------|\n",
    "| GET | Read a resource | Yes | Yes |\n",
    "| POST | Create / trigger action | No | No |\n",
    "| PUT | Replace entire resource | Yes | No |\n",
    "| PATCH | Partial update | No | No |\n",
    "| DELETE | Remove resource | Yes | No |\n\n",
    "**Idempotent** means calling the same request multiple times produces the same result. This matters for retry logic — safe to retry a `PUT` after a timeout, dangerous to retry a `POST` (might create duplicates).\n\n",
    "### **REST Request/Response Flow**\n\n",
    "```text\n",
    "Client                         Server\n",
    "  │                              │\n",
    "  │  GET /users/42               │\n",
    "  │  Accept: application/json    │\n",
    "  │  Authorization: Bearer <tok> │\n",
    "  │─────────────────────────────▶│\n",
    "  │                              │  Look up user 42\n",
    "  │                              │  in database\n",
    "  │  200 OK                      │\n",
    "  │  Content-Type: application/json\n",
    "  │  {\"id\":42,\"name\":\"Alice\"}    │\n",
    "  │◀─────────────────────────────│\n",
    "```\n\n",
    "### **REST Best Practices**\n\n",
    "- Use nouns for resources, not verbs: `/orders` not `/getOrders`\n",
    "- Use plural resource names: `/users/42` not `/user/42`\n",
    "- Return meaningful HTTP status codes: `200 OK`, `201 Created`, `400 Bad Request`, `404 Not Found`, `429 Too Many Requests`, `500 Internal Server Error`\n",
    "- Version your API: `/v1/users` so you can evolve without breaking clients\n"
