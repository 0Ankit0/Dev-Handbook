    "---\n\n",
    "## **7.3 GraphQL**\n\n",
    "**GraphQL** is a query language for APIs developed by Facebook (Meta) in 2012. Instead of many REST endpoints each returning a fixed shape, GraphQL exposes a *single endpoint* where clients specify exactly the data they need.\n\n",
    "### **The Problem GraphQL Solves**\n\n",
    "With REST, you often face:\n",
    "- **Over-fetching**: The endpoint returns 30 fields but you only need 3.\n",
    "- **Under-fetching**: One endpoint doesn't return enough data, so you make 3 more requests (the N+1 problem).\n\n",
    "**GraphQL solution**: The client declares its data requirements in the query. The server returns exactly that shape — no more, no less.\n\n",
    "```text\n",
    "REST: Multiple round-trips for a profile page\n",
    "──────────────────────────────────────────────\n",
    "GET /users/42            → user name, email\n",
    "GET /users/42/posts      → post IDs + titles\n",
    "GET /posts/7/comments    → comment count\n",
    "3 requests, 3 round trips\n",
    "\n",
    "GraphQL: One query, one response\n",
    "──────────────────────────────────────────────\n",
    "POST /graphql\n",
    "{\n",
    "  user(id: 42) {\n",
    "    name\n",
    "    email\n",
    "    posts(last: 5) {\n",
    "      title\n",
    "      commentCount\n",
    "    }\n",
    "  }\n",
    "}\n",
    "→ Exactly the fields requested, in one round trip\n",
    "```\n\n",
    "### **When to Use GraphQL**\n\n",
    "✅ Mobile apps (minimize bandwidth with precise queries)  \n",
    "✅ Frontend teams move fast without waiting for new API endpoints  \n",
    "✅ Aggregating data from multiple services in a single call  \n",
    "❌ Simple CRUD APIs where REST is perfectly adequate  \n",
    "❌ Real-time streaming (GraphQL Subscriptions add complexity)\n"
