    "## **7.4 gRPC (Google Remote Procedure Call)**\n\n",
    "**gRPC** is a high-performance RPC framework from Google. It uses **Protocol Buffers** (protobuf) for serialization and **HTTP/2** as the transport layer.\n\n",
    "### **How gRPC Differs from REST**\n\n",
    "| Aspect | REST/JSON | gRPC/Protobuf |\n",
    "|--------|-----------|---------------|\n",
    "| Protocol | HTTP/1.1 | HTTP/2 |\n",
    "| Payload | Text (JSON) | Binary (protobuf) |\n",
    "| Schema | Optional (OpenAPI) | Required (.proto file) |\n",
    "| Speed | Moderate | ~5–10× faster serialization |\n",
    "| Streaming | No (HTTP/1.1) | Bidirectional streaming |\n",
    "| Browser support | Universal | Limited (needs grpc-web) |\n\n",
    "### **gRPC Service Definition (`.proto` file)**\n\n",
    "A `.proto` file acts as a contract between client and server. gRPC generates client and server code in your target language from this schema.\n\n",
    "```text\n",
    "// user.proto\n",
    "syntax = \"proto3\";\n",
    "\n",
    "service UserService {\n",
    "  rpc GetUser (GetUserRequest) returns (User);\n",
    "  rpc ListUsers (ListUsersRequest) returns (stream User); // Server streaming\n",
    "}\n",
    "\n",
    "message GetUserRequest {\n",
    "  int64 id = 1;\n",
    "}\n",
    "\n",
    "message User {\n",
    "  int64  id    = 1;\n",
    "  string name  = 2;\n",
    "  string email = 3;\n",
    "}\n",
    "```\n\n",
    "### **gRPC Communication Patterns**\n\n",
    "```text\n",
    "┌────────────────────────────────────────────────────────────────┐\n",
    "│                   gRPC Communication Patterns                  │\n",
    "├────────────────────────────────────────────────────────────────┤\n",
    "│                                                                │\n",
    "│  1. Unary RPC (like REST)                                      │\n",
    "│     Client ──── Request ────▶ Server                          │\n",
    "│     Client ◀─── Response ─── Server                          │\n",
    "│                                                                │\n",
    "│  2. Server Streaming (e.g., live updates)                      │\n",
    "│     Client ──── Request ────▶ Server                          │\n",
    "│     Client ◀─── Response 1 ── Server                         │\n",
    "│     Client ◀─── Response 2 ── Server                         │\n",
    "│     Client ◀─── Response N ── Server                         │\n",
    "│                                                                │\n",
    "│  3. Client Streaming (e.g., uploading log chunks)              │\n",
    "│     Client ──── Request 1 ──▶ Server                          │\n",
    "│     Client ──── Request N ──▶ Server                          │\n",
    "│     Client ◀─── Response ─── Server                          │\n",
    "│                                                                │\n",
    "│  4. Bidirectional Streaming (e.g., chat, real-time telemetry)  │\n",
    "│     Client ◀──────────────── Server  (simultaneous streams)   │\n",
    "│                                                                │\n",
    "└────────────────────────────────────────────────────────────────┘\n",
    "```\n\n",
    "The diagram above shows the four gRPC call types. Bidirectional streaming is particularly powerful for real-time use cases like live dashboards or game servers.\n\n",
    "### **When to Use gRPC**\n\n",
    "✅ Internal service-to-service communication where speed matters  \n",
    "✅ Polyglot environments (protobuf generates client code in 10+ languages)  \n",
    "✅ Low-latency, high-throughput microservice communication  \n",
    "❌ Public APIs consumed by browsers (gRPC-Web adds overhead)  \n",
    "❌ Teams unfamiliar with protobuf schema management\n"
