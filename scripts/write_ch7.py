    "## **7.7 API Design Patterns**\n\n",
    "### **Versioning Strategies**\n\n",
    "API versioning lets you evolve your API without breaking existing clients.\n\n",
    "| Strategy | Example | Pros | Cons |\n",
    "|----------|---------|------|------|\n",
    "| URL path | `/v2/users` | Obvious, cacheable | URLs change |\n",
    "| Header | `API-Version: 2` | Clean URLs | Less discoverable |\n",
    "| Query param | `/users?version=2` | Easy to test | Pollutes URLs |\n",
    "| Media type | `Accept: application/vnd.api.v2+json` | RESTful | Complex |\n\n",
    "**Recommendation**: Use URL path versioning (`/v1/`, `/v2/`). It is the most explicit and easiest to test in a browser or curl.\n\n",
    "### **Pagination Patterns**\n\n",
    "Never return unbounded collections. Three common strategies:\n\n",
    "```text\n",
    "1. Offset pagination (simple, but skips are possible on fast-changing data)\n",
    "   GET /users?offset=100&limit=20\n",
    "   → Returns records 101–120\n",
    "\n",
    "2. Cursor-based pagination (stable, efficient for large datasets)\n",
    "   GET /users?cursor=eyJpZCI6MTAwfQ==&limit=20\n",
    "   Response includes: { \"next_cursor\": \"eyJpZCI6MTIwfQ==\" }\n",
    "   → Stable across inserts/deletes; used by Twitter, Facebook\n",
    "\n",
    "3. Keyset pagination (fastest; requires sortable, unique key)\n",
    "   GET /users?after_id=100&limit=20\n",
    "   → Database: WHERE id > 100 LIMIT 20 (uses index efficiently)\n",
    "```\n\n",
    "### **Rate Limiting Headers**\n\n",
    "Always communicate rate limit status to API consumers:\n\n",
    "```text\n",
    "HTTP/1.1 200 OK\n",
    "X-RateLimit-Limit:     1000    # requests per hour\n",
    "X-RateLimit-Remaining:  423    # requests left in current window\n",
    "X-RateLimit-Reset:  1701388800  # Unix timestamp when window resets\n",
    "\n",
    "When exceeded:\n",
    "HTTP/1.1 429 Too Many Requests\n",
    "Retry-After: 3600\n",
    "```\n\n",
    "---\n\n",
    "## **7.8 Choosing the Right Protocol: Decision Guide**\n\n",
    "```text\n",
    "Start here: Who is the consumer?\n",
    "         │\n",
    "         ├─▶ External / browser / public API\n",
    "         │         │\n",
    "         │         ├─▶ Simple CRUD?           → REST/JSON\n",
    "         │         ├─▶ Complex queries?        → GraphQL\n",
    "         │         └─▶ Real-time push needed?  → WebSockets or SSE\n",
    "         │\n",
    "         └─▶ Internal service-to-service\n",
    "                   │\n",
    "                   ├─▶ Need maximum performance? → gRPC + protobuf\n",
    "                   ├─▶ Streaming data?            → gRPC streaming\n",
    "                   └─▶ Simple, team prefers REST?  → REST/JSON (fine!)\n",
    "```\n\n",
    "There is no universally \"best\" choice. REST dominates public APIs for good reason: simplicity and ubiquity. gRPC wins for internal microservice meshes at scale. GraphQL shines for product APIs serving multiple frontends.\n"
