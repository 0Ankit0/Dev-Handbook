    "## **7.5 WebSockets and Server-Sent Events (SSE)**\n\n",
    "HTTP is fundamentally request-response: the client initiates, the server responds. For real-time applications — live scores, collaborative editing, notifications — you need the server to *push* data to the client without the client polling.\n\n",
    "### **WebSockets**\n\n",
    "A **WebSocket** establishes a persistent, full-duplex (bidirectional) TCP connection over HTTP. After the initial HTTP upgrade handshake, both parties can send frames at any time.\n\n",
    "```text\n",
    "WebSocket Connection Lifecycle:\n",
    "\n",
    "Client                                    Server\n",
    "  │                                          │\n",
    "  │  GET /ws  HTTP/1.1                       │\n",
    "  │  Upgrade: websocket                      │\n",
    "  │  Connection: Upgrade                     │\n",
    "  │─────────────────────────────────────────▶│\n",
    "  │                                          │\n",
    "  │  101 Switching Protocols                 │\n",
    "  │  Upgrade: websocket                      │\n",
    "  │◀─────────────────────────────────────────│\n",
    "  │                                          │\n",
    "  │  [Persistent TCP connection open]        │\n",
    "  │                                          │\n",
    "  │  {\"type\": \"chat\", \"msg\": \"Hello\"}        │\n",
    "  │─────────────────────────────────────────▶│\n",
    "  │                                          │\n",
    "  │          {\"type\": \"message\", \"from\": \"Bob\"}\n",
    "  │◀─────────────────────────────────────────│\n",
    "  │         (server pushes without request)  │\n",
    "```\n\n",
    "**When to use WebSockets**: Chat applications, live collaboration (Google Docs), multiplayer games, real-time trading dashboards.\n\n",
    "### **Server-Sent Events (SSE)**\n\n",
    "**SSE** is a simpler alternative where the server can push events over a regular HTTP connection, but only in one direction (server → client). The client subscribes with a regular `GET` request and keeps the connection open.\n\n",
    "| | WebSocket | SSE |\n",
    "|-|-----------|-----|\n",
    "| Direction | Bidirectional | Server → Client only |\n",
    "| Protocol | Upgraded TCP | Plain HTTP |\n",
    "| Browser support | Universal | Universal (except IE) |\n",
    "| Reconnection | Manual | Automatic |\n",
    "| Use case | Chat, games | Notifications, live feeds |\n\n",
    "**Rule of thumb**: Use SSE for simple one-way push (news feeds, build logs, progress bars). Use WebSockets when the client also needs to send real-time data back.\n"
